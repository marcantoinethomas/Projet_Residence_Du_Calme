    private void btnValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValiderActionPerformed
        try {
            //Retourner la ligne 
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String NomBail = txtNomBail.getText();
            String NoBail = txtNoBail.getText();
            String RueBail = txtRueBail.getText();
            String TelBail = txtTelBail.getText();
            String NomLoc = txtNomLoc.getText();
            String PrenomLoc = txtPNomLoc.getText();
            String NoLoc = txtNoLoc.getText();
            String RueLoc = txtRueLoc.getText();
            String TelLoc = txtTelLoc.getText();
            String NoApp = txtNoApp.getText();
            String RueApp = txtRueApp.getText();
            String NunAApp = txtNunAApp.getText();
            String TypeApp = txtTypeApp.getText();
            int EtageApp = Integer.parseInt(txtEtageApp.getText());
            String Meuble = (String) txtMeuble.getSelectedItem();
            String Chauffage = (String) txtChauffage.getSelectedItem();
            String Parking = (String) txtParking.getSelectedItem();
            int Duree = Integer.parseInt(txtDuree.getText());
            String Debut = DateFormat.getDateInstance().format(txtDateDebut.getDate());

            String Fin = DateFormat.getDateInstance().format(txtDateFin.getDate());
            double Montant = Double.parseDouble(txtMontant.getText());
            String MontantFreq = (String) txtMontantFreq.getSelectedItem();
            double Cout = Double.parseDouble(txtCout.getText());
            String CoutFreq = (String) txtCoutFreq.getSelectedItem();
            double MontantTotal = Double.parseDouble(txtMontantTotal.getText());
            String MontantTotalFreq = (String) txtMontantTotalFreq.getSelectedItem();
            String PremierTerme = DateFormat.getDateInstance().format(txtDatePremierTerme.getDate());
            Contrat contrat = new Contrat(NomBail, NoBail, RueBail, TelBail, NomLoc, PrenomLoc, NoLoc, RueLoc, TelLoc,
                    NoApp, RueApp, NunAApp, TypeApp, EtageApp, Meuble, Chauffage,
                    Parking, Duree, Debut, Fin, Montant, MontantFreq, Cout, CoutFreq, MontantTotal, MontantTotalFreq, PremierTerme);
            String ligne = Utilitaire.retournerLigneFichier(contrat);
            ManipulationFichier.ecrireFichier("Contrats.txt", ligne);
            //Mettre a jour la disponibilite de l'appartement a partir de l'indexRowApp
            ManipulationFichier.ecrireFichier("Appartements.txt", regApp, rowIndexApp);
            JOptionPane.showMessageDialog(null,
                    "Opération réussie avec succès", "Informations sur le contrat",
                    JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,
                    "Saisie non correcte.\nVeuillez entrer les bonnes informations", "EXCEPTION",
                    JOptionPane.ERROR_MESSAGE);
            requestFocusInWindow();
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null,
                    "Saisie non correcte.\nVeuillez vérifier les informations", "EXCEPTION",
                    JOptionPane.ERROR_MESSAGE);
            requestFocusInWindow();
        }
    }//GEN-LAST:event_btnValiderActionPerformed

