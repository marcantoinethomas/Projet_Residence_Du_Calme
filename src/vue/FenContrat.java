    private void tblListeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListeMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblListe.getModel();
        int count = tblListe.getRowCount();
        rowIndexClient = tblListe.getSelectedRow();
        for (int i = 0; i < count; i++) {
            if (i != rowIndexClient) {
                tblListe.setValueAt(false, i, 10);
            }
            if ((Boolean) model.getValueAt(rowIndexClient, 10)) {
                isClientSelect = true;
            }
        }
    }//GEN-LAST:event_tblListeMouseClicked

    private void tblListeAppMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListeAppMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblListeApp.getModel();
        int count = tblListeApp.getRowCount();
        rowIndexApp = tblListeApp.getSelectedRow();
        for (int i = 0; i < count; i++) {
            if (i != rowIndexApp) {
                tblListeApp.setValueAt(false, i, 10);
            }
            if ((Boolean) model.getValueAt(rowIndexApp, 10)) {
                isAppSelect = true;
            }
        }
        loaderImages(rowIndexApp, lblImageApp);
    }//GEN-LAST:event_tblListeAppMouseClicked

    private void jTabGlobalStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabGlobalStateChanged
        //Initialisation
        if (jTabGlobal.getSelectedIndex() == 0) {
            isClientSelect = false;
            isAppSelect = false;
        }

        if (jTabGlobal.getSelectedIndex() == 1) {
            //Liste des appartements
            regApp = new RegistreApp();
            ManipulationFichier.lireFichierApp("Appartements.txt", regApp, tblListeApp);
        }
        //Contrat si Client et Appartement selectionnés
        if (jTabGlobal.getSelectedIndex() == 2 && isClientSelect == true && isAppSelect == true) {
            Utilitaire.copierDonneesToContrat(ObjProprio, regClient, rowIndexClient, regApp, rowIndexApp, txtNomBail, txtNoBail, txtRueBail, txtTelBail,
                    txtNomLoc, txtPNomLoc, txtNoLoc, txtRueLoc, txtTelLoc,
                    txtNoApp, txtRueApp, txtNunAApp, txtTypeApp, txtEtageApp);
        }
    }//GEN-LAST:event_jTabGlobalStateChanged

