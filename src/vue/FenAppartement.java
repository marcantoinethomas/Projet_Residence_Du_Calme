    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        try {
            //Ajout d'un appartement
            int noCivique = Integer.parseInt(txtNoCivique.getText());
            String adresse = txtAdresse.getText();
            String codePostal = txtCodePostal.getText();
            String noApp = txtNoAppartement.getText();
            String typeApp = (String)cbxTypeApp.getSelectedItem();
            Appartement app = new Appartement(noApp, noCivique, adresse, codePostal, typeApp);
            resi.ajouterAppartement(app, tblListe);
        } catch (DataException ex) {
            JOptionPane.showMessageDialog(null,
                    "Appartement existe d√©ja", "EXCEPTION",
                    JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,
                    "Saisie non correcte.\nVeuillez entrer les bonnes informations", "EXCEPTION",
                    JOptionPane.ERROR_MESSAGE);
            requestFocusInWindow();
        }
    }//GEN-LAST:event_btnAjouterActionPerformed

